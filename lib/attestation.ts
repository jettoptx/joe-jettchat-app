/** Tier labels matching on-chain u8 values */
export const ACCESS_TIERS = ["basic", "mojo", "dojo", "space_cowboy"] as const;
export type AccessTier = (typeof ACCESS_TIERS)[number];

export interface AccessPassData {
  xHandle: string;
  authority: PublicKey;
  grantedAt: number;
  expiresAt: number;
  tier: AccessTier;
  revoked: boolean;
  paymentRef: string;
  pda: PublicKey;
}

/**
 * Derive the AccessPass PDA for a given X handle.
 * Seeds: [b"jett_access", x_handle.as_bytes()]
 */
export function deriveAccessPassPDA(xHandle: string): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from("jett_access"), Buffer.from(xHandle)],
    ATTESTATION_PROGRAM_ID
  );
}

/**
 * Check whether an X handle has a valid (non-revoked, non-expired) AccessPass on-chain.
 *
 * Returns the AccessPass data if valid, or null if not found / expired / revoked.
 * This is the primary gate check for Stripe-paid users who don't hold JTX.
 */
export async function checkAccessPass(
  connection: Connection,
  xHandle: string
): Promise<AccessPassData | null> {
  try {
    const [pda] = deriveAccessPassPDA(xHandle);
    const accountInfo = await connection.getAccountInfo(pda);

    if (!accountInfo || !accountInfo.data || accountInfo.data.length === 0) {
      return null;
    }

    // Manual deserialization (avoids full Anchor IDL dependency for read-only check)
    // Layout: 8 (discriminator) + 32 (x_handle) + 32 (authority) + 8 (granted_at)
    //       + 8 (expires_at) + 1 (tier) + 1 (revoked) + 1 (bump) + 32 (payment_ref)
    const data = accountInfo.data;
    const offset = 8; // skip Anchor discriminator

    // x_handle: [u8; 32] — trim trailing zeros
    const handleBytes = data.slice(offset, offset + 32);
    const handleEnd = handleBytes.indexOf(0);
    const handle = new TextDecoder().decode(
      handleBytes.slice(0, handleEnd === -1 ? 32 : handleEnd)
    );

    // authority: Pubkey (32 bytes)
    const authority = new PublicKey(data.slice(offset + 32, offset + 64));

    // granted_at: i64 (8 bytes LE)
    const grantedAt = Number(data.readBigInt64LE(offset + 64));

    // expires_at: i64 (8 bytes LE)
    const expiresAt = Number(data.readBigInt64LE(offset + 72));

    // tier: u8
    const tierValue = data[offset + 80];
    const tier: AccessTier = ACCESS_TIERS[tierValue] ?? "basic";

    // revoked: bool
    const revoked = data[offset + 81] !== 0;

    // bump: u8 (offset + 82, skip)

    // payment_ref: [u8; 32] — trim trailing zeros
    const refBytes = data.slice(offset + 83, offset + 115);
    const refEnd = refBytes.indexOf(0);
    const paymentRef = new TextDecoder().decode(
      refBytes.slice(0, refEnd === -1 ? 32 : refEnd)
    );

    const now = Math.floor(Date.now() / 1000);

    // Check validity
    if (revoked || expiresAt <= now) {
      return null;
    }

    return {
      xHandle: handle,
      authority,
      grantedAt,
      expiresAt,
      tier,
      revoked,
      paymentRef,
      pda,
    };
  } catch (err) {
    console.error("[checkAccessPass] Error:", err);
    return null;
  }
}

// ─── PDA Derivation ───────────────────────────────────────────────────────────

/** Derive the ChannelState PDA for a given slug */
export function channelStatePDA(slug: string): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from("channel"), Buffer.from(slug)],
    ATTESTATION_PROGRAM_ID
  );
}

/** Derive the Attestation PDA for a given channel + batch index */
export function attestationPDA(
  channelStatePubkey: PublicKey,
  batchIndex: bigint
): [PublicKey, number] {
  const batchBytes = Buffer.alloc(8);
  batchBytes.writeBigUInt64LE(batchIndex);
  return PublicKey.findProgramAddressSync(
    [Buffer.from("attestation"), channelStatePubkey.toBuffer(), batchBytes],
    ATTESTATION_PROGRAM_ID
  );
}

// ─── SHA-256 via Web Crypto ───────────────────────────────────────────────────

/** Hash a UTF-8 string to a 32-byte SHA-256 digest */
export async function sha256Str(input: string): Promise<Uint8Array> {
  const data = new TextEncoder().encode(input);
  const buf = await crypto.subtle.digest("SHA-256", data);
  return new Uint8Array(buf);
}

/** Hash raw bytes to a 32-byte SHA-256 digest */
export async function sha256Bytes(input: Uint8Array): Promise<Uint8Array> {
  const buf = await crypto.subtle.digest("SHA-256", input as unknown as ArrayBuffer);
  return new Uint8Array(buf);
}

// ─── Merkle Tree ──────────────────────────────────────────────────────────────

export interface MerkleTree {
  /** Hex-encoded root hash */
  root: string;
  /** Map from hex-encoded leaf hash → array of hex-encoded sibling hashes (leaf→root order) */
  proofs: Map<string, string[]>;
  /** Ordered leaf hashes (hex) */
  leaves: string[];
}

/**
 * Build a sorted-pair SHA-256 Merkle tree from an array of message hash hex strings.
 *
 * "Sorted pair" means: when hashing two siblings, the lexicographically smaller
 * hash always goes on the left. This makes the tree canonical regardless of
 * insertion order and matches the on-chain verify_message logic.
 *
 * @param messageHashes - Array of 64-char hex strings (32-byte SHA-256 hashes)
 * @returns MerkleTree with root (hex) and proofs map
 */
export async function buildMerkleTree(
  messageHashes: string[]
): Promise<MerkleTree> {
  if (messageHashes.length === 0) {
    throw new Error("buildMerkleTree: cannot build tree from empty array");
  }

  // Hash each message ID again so callers can pass raw message IDs (strings)
  // OR pre-computed hex hashes — we re-hash to normalise.
  const leaves: Uint8Array[] = await Promise.all(
    messageHashes.map((h) => sha256Str(h))
  );

  const leafHex = leaves.map(toHex);
  const proofs = new Map<string, string[]>(leafHex.map((h) => [h, []]));

  // Build tree bottom-up
  let level: Uint8Array[] = [...leaves];

  // Pad to even number at each level (duplicate last node)
  while (level.length > 1) {
    if (level.length % 2 !== 0) {
      level.push(level[level.length - 1]);
    }

    const nextLevel: Uint8Array[] = [];

    for (let i = 0; i < level.length; i += 2) {
      const left = level[i];
      const right = level[i + 1];

      // Record siblings for proof path
      const [sortedLeft, sortedRight] =
        compareBytes(left, right) <= 0 ? [left, right] : [right, left];

      // For every leaf in the left half, the sibling is from the right half
      recordSiblings(proofs, level.slice(0, i + 1).concat(level.slice(i, i + 1)), toHex(right), leafHex);
      recordSiblings(proofs, level.slice(i + 1, i + 2), toHex(left), leafHex);

      const combined = new Uint8Array(64);
      combined.set(sortedLeft, 0);
      combined.set(sortedRight, 32);
      nextLevel.push(await sha256Bytes(combined));
    }

    level = nextLevel;
  }

  // Recompute proofs cleanly with a simpler bottom-up pass
  const cleanProofs = await computeProofs(leaves);

  return {
    root: toHex(level[0]),
    proofs: cleanProofs,
    leaves: leafHex,
  };
}

/** Internal: compute proofs by re-running the tree and tracking paths cleanly */
async function computeProofs(
  leaves: Uint8Array[]
): Promise<Map<string, string[]>> {
  const proofMap = new Map<string, string[]>(
    leaves.map((l) => [toHex(l), []])
  );

  // Track which original leaf index corresponds to each node
  type NodeWithOrigins = { hash: Uint8Array; origins: number[] };

  let level: NodeWithOrigins[] = leaves.map((hash, i) => ({
    hash,
    origins: [i],
  }));

  while (level.length > 1) {
    if (level.length % 2 !== 0) {
      level.push({ hash: level[level.length - 1].hash, origins: [] });
    }

    const nextLevel: NodeWithOrigins[] = [];

    for (let i = 0; i < level.length; i += 2) {
      const left = level[i];
      const right = level[i + 1];

      // Each leaf in left subtree gets right sibling added to its proof
      for (const idx of left.origins) {
        const leafHex = toHex(leaves[idx]);
        proofMap.get(leafHex)!.push(toHex(right.hash));
      }
      // Each leaf in right subtree gets left sibling
      for (const idx of right.origins) {
        if (idx < leaves.length) {
          // skip padding duplicates
          const leafHex = toHex(leaves[idx]);
          proofMap.get(leafHex)!.push(toHex(left.hash));
        }
      }

      // Sort pair for canonical hash
      const [sortedLeft, sortedRight] =
        compareBytes(left.hash, right.hash) <= 0
          ? [left.hash, right.hash]
          : [right.hash, left.hash];

      const combined = new Uint8Array(64);
      combined.set(sortedLeft, 0);
      combined.set(sortedRight, 32);

      nextLevel.push({
        hash: await sha256Bytes(combined),
        origins: [...left.origins, ...right.origins.filter((i) => i < leaves.length)],
      });
    }

    level = nextLevel;
  }

  return proofMap;
}


export interface ChannelStateData {
  slug: string;
  authority: PublicKey;
  jtxMint: PublicKey;
  messageCount: bigint;
  batchCount: bigint;
  lastMerkleRoot: string; // hex
  lastAttestationTs: number;
  jtxStakeRequired: bigint;
  optxMintedTotal: bigint;
}

/** Fetch and decode the ChannelState for a given slug */
export async function fetchChannelState(
  connection: Connection,
  wallet: AnchorWallet,
  channelSlug: GatedChannel
): Promise<ChannelStateData | null> {
  const provider = new AnchorProvider(connection, wallet, {
    commitment: "confirmed",
  });
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const program = new Program(IDL as any, provider);
  const [pda] = channelStatePDA(channelSlug);

  try {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const raw = await (program.account as any).channelState.fetch(pda);
    return {
      slug: raw.slug,
      authority: raw.authority,
      jtxMint: raw.jtxMint,
      messageCount: BigInt(raw.messageCount.toString()),
      batchCount: BigInt(raw.batchCount.toString()),
      lastMerkleRoot: toHex(new Uint8Array(raw.lastMerkleRoot)),
      lastAttestationTs: raw.lastAttestationTs.toNumber(),
      jtxStakeRequired: BigInt(raw.jtxStakeRequired.toString()),
      optxMintedTotal: BigInt(raw.optxMintedTotal.toString()),
    };
  } catch {
    return null;
  }
}

// ─── Utilities ────────────────────────────────────────────────────────────────

/** Convert Uint8Array to lowercase hex string */
function toHex(bytes: Uint8Array): string {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}

/** Convert hex string to Uint8Array */
function fromHex(hex: string): Uint8Array {
  const clean = hex.startsWith("0x") ? hex.slice(2) : hex;
  const result = new Uint8Array(clean.length / 2);
  for (let i = 0; i < result.length; i++) {
    result[i] = parseInt(clean.slice(i * 2, i * 2 + 2), 16);
  }
  return result;
}

/** Lexicographic byte comparison — returns negative, 0, or positive */
function compareBytes(a: Uint8Array, b: Uint8Array): number {
  const len = Math.min(a.length, b.length);
  for (let i = 0; i < len; i++) {
    if (a[i] !== b[i]) return a[i] - b[i];
  }
  return a.length - b.length;
}


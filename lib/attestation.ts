/**
 * Verify a message hash is included in a Merkle root.
 * Pure client-side — mirrors on-chain compute_merkle_root logic exactly.
 *
 * @param messageHash - 64-char hex string of the message hash
 * @param proof - ordered array of 64-char hex sibling hashes (leaf→root)
 * @param root - 64-char hex string of the expected root
 */
export async function verifyMessage(
  messageHash: string,
  proof: string[],
  root: string
): Promise<boolean> {
  let current = fromHex(messageHash);

  for (const siblingHex of proof) {
    const sibling = fromHex(siblingHex);

    const [sortedLeft, sortedRight] =
      compareBytes(current, sibling) <= 0
        ? [current, sibling]
        : [sibling, current];

    const combined = new Uint8Array(64);
    combined.set(sortedLeft, 0);
    combined.set(sortedRight, 32);

    current = await sha256Bytes(combined);
  }

  return toHex(current) === root.toLowerCase();
}

// ─── On-chain Submission ──────────────────────────────────────────────────────

export interface AttestationResult {
  /** Solana transaction signature */
  signature: string;
  /** The Merkle root that was attested (hex) */
  merkleRoot: string;
  /** The batch index this attestation received */
  batchIndex: bigint;
  /** Unix timestamp from Solana Clock */
  timestamp: number;
}

export interface AttestationClient {
  connection: Connection;
  wallet: AnchorWallet;
  jtxTokenAccount: PublicKey;
}

/**
 * Submit a Merkle root attestation for a JettChat channel.
 *
 * @param client - Connection, wallet adapter, and attester's JTX token account
 * @param channelSlug - "#dojo" or "#mojo"
 * @param merkleRoot - 64-char hex root from buildMerkleTree
 * @param messageCount - number of messages in this batch
 * @returns AttestationResult with tx signature and batch metadata
 */
export async function submitAttestation(
  client: AttestationClient,
  channelSlug: GatedChannel,
  merkleRoot: string,
  messageCount: number
): Promise<AttestationResult> {
  if (messageCount <= 0) {
    throw new Error("submitAttestation: messageCount must be > 0");
  }
  if (merkleRoot.length !== 64) {
    throw new Error("submitAttestation: merkleRoot must be a 64-char hex string");
  }

  const { connection, wallet, jtxTokenAccount } = client;

  const provider = new AnchorProvider(connection, wallet, {
    commitment: "confirmed",
    preflightCommitment: "confirmed",
  });
  setProvider(provider);

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const program = new Program(IDL as any, provider);

  const [channelStatePubkey] = channelStatePDA(channelSlug);

  // Fetch current batch_count to derive the next attestation PDA
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const channelStateAccount = await (program.account as any).channelState.fetch(
    channelStatePubkey
  );
  const batchIndex: bigint = BigInt(channelStateAccount.batchCount.toString());

  const [attestationPubkey] = attestationPDA(channelStatePubkey, batchIndex);

  const merkleRootBytes = Array.from(fromHex(merkleRoot));

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const tx = await (program.methods as any)
    .submitAttestation(merkleRootBytes, messageCount)
    .accounts({
      channelState: channelStatePubkey,
      attestation: attestationPubkey,
      attesterJtxAccount: jtxTokenAccount,
      attester: wallet.publicKey,
      tokenProgram: new PublicKey("TokenkegQfeZyiNwAJbNbGKPFXCWuBvf9Ss623VQ5DA"),
      systemProgram: SystemProgram.programId,
    })
    .rpc();

  return {
    signature: tx,
    merkleRoot,
    batchIndex,
    timestamp: Math.floor(Date.now() / 1000),
  };
}

/**
 * Initialize a ChannelState PDA. Called once per channel by the authority.
 * Only needed during deployment — not in normal user flow.
 */
export async function initializeChannel(
  client: AttestationClient,
  channelSlug: GatedChannel,
  jtxStakeRequired: number
): Promise<string> {
  const { connection, wallet } = client;

  const provider = new AnchorProvider(connection, wallet, {
    commitment: "confirmed",
  });
  setProvider(provider);

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const program = new Program(IDL as any, provider);

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const tx = await (program.methods as any)
    .initializeChannel(channelSlug, new BN(jtxStakeRequired))
    .accounts({
      channelState: channelStatePDA(channelSlug)[0],
      jtxMint: JTX_MINT,
      authority: wallet.publicKey,
      systemProgram: SystemProgram.programId,
    })
    .rpc();

  return tx;
}

